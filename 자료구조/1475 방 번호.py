# 다솜이는 자기 방 번호를 예쁜 플라스틱 숫자로 문에 붙이려고 한다. 
# 다솜이의 옆집에서는 플라스틱 숫자를 한 세트로 판다. 
# 한 세트에는 0번부터 9번까지 숫자가 하나씩 들어있다. 
# 다솜이의 방 번호가 주어졌을 때, 필요한 세트의 개수의 최솟값을 출력하시오. 
# (6은 9를 뒤집어서 이용할 수 있고, 9는 6을 뒤집어서 이용할 수 있다.)

n = list(input())
l=[0]*10

for w in n:
    if w=='6' or w=='9':
        # 6 갯수 추가 -> 9 갯수 추가 -> 6 갯수 추가 ... 반복
        if l[6]==l[-1]: 
            l[6]+=1
        else:
            l[-1]+=1
    else:
        l[int(w)]+=1
        
print(max(l))